						<h3 class='nome-tartaruga'>Carolina</h3>
						<p class='texto-historia'>
							<strong>Recupero:</strong> spiaggia antistante l'Acquario di Cattolica, 23 marzo 2018</p>
						<p class='texto-historia'>
							<strong>Condizioni:</strong> Carolina è una piccola tartaruga, al ritrovamento misurava appena 22 cm per 1,4 kg! Nei giorni del suo ritrovamento circa 15 tartarughe in ipotermia si sono spiaggiate a causa delle forti mareggiate. Le tartarughe, infatti, sono rettili "a sangue freddo" e soffrono molto gli sbalzi termici, non essendo in grado di termoregolarsi, specialmente quando sono di piccole dimensioni.
						</p>
						<p class='texto-historia'><strong>Cure:</strong> dopo aver recuperato lo stato di ipotermia, un veterinario specializzato in oculistica ne ha diagnosticato la cecità, probabilmente in seguito a un'infezione contratta in mare. Il suo progetto di recupero ha un decorso delicato e peculiare in virtù della sua particolare condizione di disabilità.</p>
					</div>

