						<h3 class='nome-tartaruga'>Faustina</h3>
						<p class='texto-historia'>
							<strong>Recupero:</strong> Porto Barricata (Rovigo), 22 luglio 2018 
						<p class='texto-historia'>
							<strong>Condizioni:</strong> al recupero presentava uno sfondamento della calotta cranica probabilmente per mano umana. </p>
						<p class='texto-historia'><strong>Cure:</strong> nonostante un intervento chirurgico, presenta gravi problemi neurologici: se non stimolata resta catatonica a galleggiare nella vasca. Per questo è stata portata al Centro di recupero di Riccione dove ha cominciato un percorso di riabilitazione. La sua fisioterapia consiste nel cercare di sincronizzare le pinne anteriori e posteriori con allungamenti muscolari fino a ottenere il completo rilassamento delle stesse, che altrimenti tiene serrate. Ha iniziato da poco a mangiare autonomamente piccole quantità di pesce. A completa guarigione potrà fare ritorno in mare.</p>
