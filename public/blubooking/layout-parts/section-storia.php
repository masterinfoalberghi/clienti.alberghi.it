						<h3 class='nome-tartaruga'>Alessandra</h3>
						<p class='texto-historia'><strong>Recupero:</strong> Lido di Volano, agosto 2018<br/></p>
						<p class='texto-historia'>
							<strong>Condizioni:</strong> due ferite da elica molto profonde, una al cranio e una al carapace, alla TAC ha rivelato anche un amo nell'esofago.
						</p>
						<p class='texto-historia'>	<strong>Cure:</strong> operata alla Clinica Veterinaria Modena Sud dal dottor Giordano Nardini, grazie alla collaborazione di Associazione Benessere Animale Onlus. Una volta cicatrizzata la ferita al cranio con laserterapia e ricoperta con una speciale resina, è tornata al Centro di recupero di Riccione per il percorso di fisioterapia, che consiste in movimenti sincronizzati delle pinne anteriori e posteriori, movimenti della ranfoteca (becco) e dei muscoli del collo. Dopo alimentazione forzata fino ad aprile, ora mangia autonomamente. La sua riabilitazione speriamo si concluda con la completa guarigione e il ritorno in mare!
						</p>
