							<h2>Come funziona l'app IO</h2>

						<h3>1. Prima di accedere ottieni l'ISEE </h3>
						<p>Per richiedere il Bonus Vacanze <strong>devi avere un ISEE valido (ordinario o corrente)</strong>.</p>
						

						<h3>2. Accedi con SPID</h3>
						<p>Per accedere all'app inserisci le tue credenziali SPID (o CIE).

						<h3>3. Richiedi il bonus</h3>
						<p>
							Vai nella <strong>sezione Pagamenti</strong>. Clicca su bonus e <strong>scegli “Bonus Vacanze”</strong> dalla lista di quelli disponibili. <br/>Segui i passaggi e completa la richiesta.</p>

						<h3>4. Ottieni subito il responso</h3>
						 <p>
							   	L’app verifica subito i requisiti e può dare queste risposte:
							   	<ul>
							   		<li><i>Richiesta valida:</i><br/>
									compare l’importo del bonus e il nome dei familiari che possono utilizzarlo. </li>
									<li><i>Richiesta valida ma bonus già attivato sullo stesso nucleo familiare:</i></br>
									bonus già richiesto da un altro familiare.</li>
									<li><i>Richiesta valida, DSU con omissioni o difformità:</i><br/>
									 requisiti validi, si può procedere con la richiesta, sarà poi necessario presentare la documentazione completa relativa alla DSU.</li>
									<li><i>Soglia ISEE superata:</i></br>
									con ISEE superiore a 40mila euro non è possibile inviare la richiesta</li>
									<li><i>DSU assente:</i></br>
									non è possibile inviare la richiesta.</li>	
								</ul>
								</p> 
							</div>

