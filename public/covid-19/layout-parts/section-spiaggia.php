					<p>
						Gli stabilimenti balneari sulla Riviera Romagnola sono aperti al pubblico e operativi. L’attività balneare e il servizio di salvamento sono garantiti dal 13 giugno al 13 settembre 2020.</p>
						Anche sulle spiagge e in mare rimane l’obbligo di rispettare la distanza di sicurezza di 1 metro e il divieto di assembramento, nel rispetto dell'<a href="https://bur.regione.emilia-romagna.it/bur/area-bollettini/bollettini-in-lavorazione/n-165-del-22-05-2020-parte-seconda.2020-05-22.5144053445/approvazione-ordinanza-balneare-straordinaria-per-il-contenimento-degli-effetti-del-coronavirus-n-1-2020/ordinanza-balneare-straordinar.2020-05-22.1590131532" target="_blank">ordinanza balneare regionale 2020 con misure straordinarie per il contenimento degli effetti del coronavirus</a>.					
					</p>

					<p>
						In sintesi, ecco come sarà la spiaggia per l'estate 2020:
						<ul>
							<li>Più spazio tra gli ombrelloni: la superficie minima per ogni ombrellone è di 12 mq</li>
							<li>Lettini distanziati: la distanza minima tra i lettini posizionati sulla battigia è 1,5 m </li>
							<li>Igiene: gli stabilimenti balneari devono assicurare pulizia giornaliera ordinaria e disinfezione dei servizi igienici, le attrezzature devono essere disinfettate a ogni cambio clientela e a fine giornata.</li>
							<li>Aree giochi per bambini: accessibili sotto sorveglianza diretta dei genitori, con possibilità di ingressi contingentati (<a href="./vacanze-bambini/">Leggi la guida per viaggiare con i bambini</a></li>
							<li>Beach volley, beach tennis e beach soccer: consentiti nelle aree polifunzionali degli stabilimenti balneari con distanza di 2 metri tra i giocatori</li>
							<li>Vietati giochi e attività sportive che non consentono il rispetto del distanziamento</li>
							<li>Sulle spiaggie e nel mare devono essere rispettate le regole di distanziamento fisico di almeno 1 metro e il divieto di assembramento, queste distanze sono derogate per i componenti di uno stesso nucleo familiare e per chi soggiorna nella stessa camera di hotel o nella stessa unità abitativa</li>
							
						</ul>
					</p>

