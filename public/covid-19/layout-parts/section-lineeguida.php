					<h3>Vacanze in hotel in Riviera Romagnola - estate 2020</h3>			
					<p>
						Il <strong>18 maggio le strutture ricettive della riviera romagnola hanno riaperto</strong>.

						Tutte le strutture seguono le <a href="https://www.comune.rimini.it/sites/comune.rimini.it/files/ordinanza_17_maggio_2020_decreto_n._82_2020.pdf" target="_blank">linee guida per la riapertura degli hotel e delle strutture ricettive alberghiere</a><strong> publicate il 17 maggio dalla Regione Emilia Romagna</strong>.
					</p>

					<p>
					In sintesi ecco cosa troverai negli hotel:
					
					<p><strong>Informazioni su regole e percorsi</strong>
					<br/>In hotel si troveranno informazioni dettagliate sulle regole di accesso e di comportamento per evitare il rischio di contagio. È di fondamentale importanza la responsabilità individuale da parte dei clienti nell'adozione di comportamenti rispettosi delle misure di sicurezza e prevenzione.</p>

					<p><strong>Igienizzazione</strong><br/>
					Tutti gli ambienti interni ed esterni della struttura saranno adeguatamente puliti e disinfettati durante il giorno, dagli ambienti comuni agli ascensori, a tutti gli arredi che vengono toccati di frequente. </p>

					<p><strong>Organizzazione degli spazi per garantire le distanze</strong><br/>
					Per aiutare gli ospiti a rispettare le distanze di sicurezza, gli hotel hanno ripensato i loro ambienti interni e valorizzato quelli esterni.</p>

					<p><strong>Pulizia camere</strong><br/>
					A ogni cambio cliente le camere e gli oggetti toccati più spesso saranno igienizzati con particolare attenzione: non solo lenzuola o biancheria, ma anche maniglie delle porte e dei mobili, interruttori e altri arredi delle camere. È consigliato evitare tutte le possibilità di pulizia e riassetto giornalieri.</p>

					<p><strong>Sala ristorante, più spazio e meno buffet</strong><br/>
					Anche la ristorazione cambia per garantire il massimo distanziamento fisico. Tavoli più distanti (almeno 1 m) in sala ristorante, personale munito di dispositivi di protezione individuale, servizio al tavolo invece che a buffet. Dove possibile i pasti possono essere serviti in camera o in forme alternative come da asporto, da consumare ad esempio in spiaggia.</p>

					<p><strong>Dpi e mascherine</strong><br/>
					A disposizione degli ospiti saranno dislocati dispenser di gel igienizzante per le mani. L’uso della mascherina è obbligatorio dove non è possibile mantenere il distanziamento di almeno un metro.</p>
					</p>
					 
